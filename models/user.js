///////////////////////////////////////////////////////////
// Import our dependencies
///////////////////////////////////////////////////////////
const { Schema, model } = require('./connection.js')


///////////////////////////////////////////////////////////
// Define user schema and model
///////////////////////////////////////////////////////////
const UserSchema = new Schema(
	{
		username: { 
			type: String, 
			required: true, 
			unique: true 
		},
		password: { 
			type: String, 
			required: true 
		}
	},
	{ timestamps: true }
)

// create the model
const User = model('User', UserSchema)

///////////////////////////////////////////////////////////
// Export our model
///////////////////////////////////////////////////////////
module.exports = User
